#include <jni.h>
#include <string>
#include <vector>

namespace {
std::string xorCombine(const std::vector<unsigned char>& data, const std::vector<unsigned char>& pad) {
    std::string out;
    out.reserve(data.size());
    for (size_t i = 0; i < data.size(); ++i) {
        out.push_back(static_cast<char>(data[i] ^ pad[i % pad.size()]));
    }
    return out;
}
}

